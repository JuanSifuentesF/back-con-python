# Escribe un programa que permita calcular el área de un rectángulo
# Los datos deben ser escritos por el usuario
# Ärea = Base × Altura

base = float(input("Ingrese la base: "))
print(base)

altura = float(input("Ingrese la altura: "))
print(altura)

area = base * altura

print(f"El area del rectángulo es de: ",area)

